_SECURITY_FILE_PATTERNS = re.compile(
    r"(?:auth|password|token|encrypt|decrypt|jwt|oauth|session|"
    r"middleware[/\\]auth|guard|permission|credential|secret|"
    r"csrf|cors|sanitiz|hash|bcrypt|argon|scrypt|cert|ssl|tls)",
    re.IGNORECASE,
)


# ── Data models ──────────────────────────────────────────────────

@dataclass
class FileInfo:
    path: Path
    relative: str
    size: int
    language: str
    imports: list[str] = field(default_factory=list)
    exports: list[str] = field(default_factory=list)
    relevance_score: float = 0.0
    is_generated: bool = False


@dataclass
class ProjectIndex:
    root: Path
    total_files: int = 0
    total_size: int = 0
    files: list[FileInfo] = field(default_factory=list)
    dependency_graph: dict[str, list[str]] = field(default_factory=dict)
    entry_points: list[str] = field(default_factory=list)
    languages: dict[str, int] = field(default_factory=dict)
    framework: str = ""
    summary: str = ""
    tier: str = "tiny"

    @property
    def file_listing(self) -> str:
        """Compact file tree for agent context."""
        lines = [f"Project: {self.root.name} ({self.total_files} files, {self.total_size:,} bytes)"]
        lines.append(f"Languages: {', '.join(f'{k}({v})' for k, v in sorted(self.languages.items(), key=lambda x: -x[1]))}")
        if self.framework:
            lines.append(f"Framework: {self.framework}")
        if self.entry_points:
            lines.append(f"Entry points: {', '.join(self.entry_points[:10])}")
        lines.append("\nFile tree:")
        for fi in self.files[:200]:
            tag = " [GEN]" if fi.is_generated else ""
            lines.append(f"  {fi.relative} ({fi.size:,}b, {fi.language}){tag}")
        if self.total_files > 200:
            lines.append(f"  ... and {self.total_files - 200} more files")
        return "\n".join(lines)


