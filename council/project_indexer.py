def detect_language(path: Path) -> str:
    name = path.name.lower()
    if name == "dockerfile" or name.startswith("dockerfile."):
        return "docker"
    if name == "makefile":
        return "make"
    return _EXT_TO_LANG.get(path.suffix.lower(), "other")


# ── Import extraction (#15 — fixed relative imports + TS) ────────

def extract_python_imports(content: str, file_relative: str = "") -> list[str]:
    """Extract imports from Python source using AST, including relative imports."""
    try:
        tree = ast.parse(content)
        imports = []
        for node in ast.walk(tree):
            if isinstance(node, ast.Import):
                for alias in node.names:
                    imports.append(alias.name)
            elif isinstance(node, ast.ImportFrom):
                if node.module:
                    if node.level > 0 and file_relative:
                        # Resolve relative import
                        parts = file_relative.replace("\\", "/").split("/")
                        # Go up `level` directories
                        base_parts = parts[:-1]  # remove filename
                        for _ in range(node.level - 1):
                            if base_parts:
                                base_parts.pop()
                        resolved = ".".join(base_parts + [node.module]) if base_parts else node.module
                        imports.append(resolved)
                    else:
                        imports.append(node.module)
                elif node.level > 0 and file_relative:
                    # `from . import X` — import from same package
                    parts = file_relative.replace("\\", "/").split("/")
                    base_parts = parts[:-1]
                    for _ in range(node.level - 1):
                        if base_parts:
                            base_parts.pop()
                    if base_parts:
                        imports.append(".".join(base_parts))
        return imports
    except SyntaxError:
        return re.findall(r"^(?:from|import)\s+([\w.]+)", content, re.MULTILINE)


def extract_js_imports(content: str) -> list[str]:
    """Extract imports from JS/TS source, including `import type`."""
    patterns = [
        r"import\s+(?:type\s+)?.*?from\s+['\"](.+?)['\"]",
        r"import\s*\(\s*['\"](.+?)['\"]",
        r"require\s*\(\s*['\"](.+?)['\"]",
        r"export\s+.*?from\s+['\"](.+?)['\"]",
    ]
    imports = []
    for p in patterns:
        imports.extend(re.findall(p, content))
    return imports


def extract_imports(content: str, language: str, file_relative: str = "") -> list[str]:
    """Extract imports based on language."""
    if language == "python":
        return extract_python_imports(content, file_relative)
    elif language in ("javascript", "typescript", "react", "react-ts"):
        return extract_js_imports(content)
    elif language == "go":
        return re.findall(r'"([\w./]+)"', content)
    elif language == "rust":
        return re.findall(r"use\s+([\w:]+)", content)
    elif language == "java":
        return re.findall(r"import\s+([\w.]+)", content)
    return []


# ── Entry point detection ────────────────────────────────────────

_ENTRY_POINT_NAMES = {
    "main.py", "app.py", "index.py", "server.py", "cli.py", "__main__.py",
    "index.js", "index.ts", "main.js", "main.ts", "app.js", "app.ts",
    "server.js", "server.ts", "main.go", "main.rs", "main.java",
    "manage.py", "wsgi.py", "asgi.py",
}

_ENTRY_POINT_PATTERNS = [
    re.compile(r'if\s+__name__\s*==\s*["\']__main__["\']'),
    re.compile(r"func\s+main\s*\("),
    re.compile(r"fn\s+main\s*\("),
    re.compile(r"public\s+static\s+void\s+main"),
]


def is_entry_point(path: Path, content: str) -> bool:
    if path.name.lower() in _ENTRY_POINT_NAMES:
        return True
    for p in _ENTRY_POINT_PATTERNS:
        if p.search(content):
            return True
    return False


