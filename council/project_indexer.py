"""Project indexer: dependency graph, summary generation, smart file selection.

Tiered context strategy:
  - Tiny   (<50 files):    Load all files directly
  - Small  (50-200 files):  Load all + project summary header
  - Medium (200-1000 files): Smart selection (top N by relevance) + summary
  - Large  (1000-10000):   Summary + changed files + dependency chain
  - Huge   (>10000 files): Tool-based exploration (agent navigates on-demand)
"""
